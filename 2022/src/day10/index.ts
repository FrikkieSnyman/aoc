import run from "aocrunner";
type AddInstruction = ["addx", number];
type NoopInstruction = ["noop"];
type Instruction = AddInstruction | NoopInstruction;
const parseInput = (rawInput: string): Instruction[] =>
  rawInput.split("\n").map((l) => {
    const split = l.split(" ");
    return [split[0], split[1] ? parseInt(split[1]) : undefined] as Instruction;
  });

const part1 = (rawInput: string) => {
  const input = parseInput(rawInput);
  let signalSums = 0;
  let x = 1;
  let index = 0;
  let cycle = 1;
  let instruction = input[index];
  const incrementCycle = () => {
    if ((cycle + 20) % 40 === 0) {
      signalSums += cycle * x;
    }
    cycle++;
  };
  while (instruction) {
    switch (instruction[0]) {
      case "addx":
        incrementCycle();
        incrementCycle();
        x += instruction[1];
        break;
      case "noop":
        incrementCycle();
        break;
    }
    instruction = input[++index];
  }
  return signalSums;
};

const part2 = (rawInput: string) => {
  const input = parseInput(rawInput);
  let x = 1;
  let index = 0;
  let cycle = 1;
  let instruction = input[index];
  let screen = "";
  const processCycle = () => {
    const pixelIndex = (cycle - 1) % 40;
    if (Math.abs(x - pixelIndex) <= 1) {
      screen += "#";
    } else {
      screen += ".";
    }
    if (pixelIndex === 39) {
      screen += "\n";
    }
    cycle++;
  };
  while (instruction) {
    switch (instruction[0]) {
      case "addx":
        processCycle();
        processCycle();
        x += instruction[1];
        break;
      case "noop":
        processCycle();
        break;
    }
    instruction = input[++index];
  }
  return screen;
};

